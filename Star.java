class Star{
public static void main(String args[]){
for(int i=0;i<=2;i++){
	if(i==0){
for(int j=0;j<=i;j++){
System.out.print("  *  ");
}
	}
	
else if(i==1){
for(int j=0;j<=i;j++){
System.out.print(" *");
}
}

else if(i==2){
for(int j=0;j<=i;j++){
System.out.print("* ");
}
}

System.out.println("\n");
}


}
}