class Continue_Keyword_Demo // class
{
	public static void main(String args[]) // main fucntion
	{
	
		for(int a=1;a<=10;a++) // simple for loop
		{
			if(a==5) // if statement
			{
				continue; // continue keyword
			}
			System.out.println("a = "+a); // printing statement
		}
	
	}
}